"""
Evaluation Dashboard -- Streamlit UI for the Eight-Dimension Test Report
==========================================================================
A separate Streamlit page that loads the evaluation_report.json and displays
a visual dashboard: summary stats, per-dimension cards with pass/fail details,
failed test drill-downs, and RAGAS metric bars.

Usage:
    streamlit run evaluation_dashboard.py

Requires:
    evaluation_report.json  (generated by evaluate.py)
    evaluation_results.json (generated by evaluate.py)
"""

from __future__ import annotations

import json
import os

from dotenv import load_dotenv
load_dotenv()

import streamlit as st

REPORT_FILE = "evaluation_report.json"
RESULTS_FILE = "evaluation_results.json"

st.set_page_config(
    page_title="BVRIT FAQ - Evaluation Dashboard",
    page_icon="📊",
    layout="wide",
)


def load_data():
    if not os.path.exists(REPORT_FILE):
        st.error(
            f"⚠️ `{REPORT_FILE}` not found. Run `python evaluate.py` first "
            "to generate the evaluation report."
        )
        st.stop()

    with open(REPORT_FILE) as f:
        report = json.load(f)

    results = []
    if os.path.exists(RESULTS_FILE):
        with open(RESULTS_FILE) as f:
            results = json.load(f)

    return report, results


report, results = load_data()
s = report["summary"]
per_dim = report["per_dimension"]
ragas = report.get("ragas_scores", {})
failed = report.get("failed_cases", [])
weakest = report.get("weakest_dimension", "N/A")
recs = report.get("recommendations", [])

# ---------------------------------------------------------------------------
# Header
# ---------------------------------------------------------------------------
st.title("📊 BVRIT FAQ Chatbot — Evaluation Dashboard")
st.caption("Eight-Dimension Evaluation Report | RAG-Powered College FAQ")

# ---------------------------------------------------------------------------
# Summary Banner
# ---------------------------------------------------------------------------
col1, col2, col3, col4 = st.columns(4)
with col1:
    st.metric("Total Test Cases", s["total_test_cases"])
with col2:
    st.metric("Passed", s["passed"], delta_color="normal")
with col3:
    st.metric("Failed", s["failed"], delta_color="inverse")
with col4:
    pass_rate_color = "normal" if s["pass_rate"] >= 70 else "inverse"
    st.metric("Pass Rate", f"{s['pass_rate']}%", delta_color=pass_rate_color)

st.divider()

# ---------------------------------------------------------------------------
# Per-Dimension Cards (4x2 grid)
# ---------------------------------------------------------------------------
st.subheader("📋 Per-Dimension Results")

dim_order = sorted(per_dim.items(), key=lambda x: x[1]["dimension_number"])

# Create 4x2 grid
for i in range(0, len(dim_order), 2):
    cols = st.columns(2)
    for j, (dim_name, info) in enumerate(dim_order[i:i+2]):
        with cols[j]:
            num = info["dimension_number"]
            passed = info["passed"]
            total = info["total"]
            rate = info["pass_rate"]
            status = "✅ Pass" if info["failed"] == 0 else "❌ Fail"

            with st.container(border=True):
                st.markdown(f"**{num}. {dim_name}**")
                st.markdown(f"**{status}** — {passed}/{total} passed ({rate}%)")
                st.progress(rate / 100, text=f"Pass rate: {rate}%")

st.divider()

# ---------------------------------------------------------------------------
# Failed Test Case Drill-Down
# ---------------------------------------------------------------------------
st.subheader("🔍 Failed Test Case Details")

if failed:
    for fc in failed:
        with st.expander(f"❌ {fc['id']} — {fc['dimension']}: {fc['question'][:80]}"):
            st.markdown("**Question:**")
            st.code(fc["question"])
            st.markdown("**Expected Answer:**")
            st.code(fc["expected"])
            st.markdown("**Actual Answer:**")
            st.code(fc["actual"])
            st.markdown("**Root Cause / Reason:**")
            st.warning(fc.get("reason", "No reason provided"))
else:
    st.success("🎉 All test cases passed! No failures to display.")

st.divider()

# ---------------------------------------------------------------------------
# Weakest Dimension Recommendation
# ---------------------------------------------------------------------------
st.subheader("💡 Weakest Dimension & Recommendation")

col1, col2 = st.columns([1, 2])
with col1:
    st.error(f"**Weakest:** {weakest}")
with col2:
    for rec in recs:
        st.info(rec)

st.divider()

# ---------------------------------------------------------------------------
# RAGAS Metrics
# ---------------------------------------------------------------------------
st.subheader("📈 RAGAS Metrics")

if ragas:
    ragas_cols = st.columns(4)
    ragas_metrics = [
        ("Faithfulness", ragas.get("faithfulness", 0.0), "#2ecc71"),
        ("Answer Relevancy", ragas.get("answer_relevancy", 0.0), "#3498db"),
        ("Context Precision", ragas.get("context_precision", 0.0), "#e67e22"),
        ("Context Recall", ragas.get("context_recall", 0.0), "#9b59b6"),
    ]

    for col, (name, val, color) in zip(ragas_cols, ragas_metrics):
        with col:
            st.markdown(f"**{name}**")
            st.markdown(
                f'<div style="background-color:{color}20; border-radius:8px; padding:10px;">'
                f'<span style="font-size:2em; font-weight:bold; color:{color};">{val:.2f}</span>'
                f'</div>',
                unsafe_allow_html=True,
            )
            st.progress(val, text="")

    # Diagnosis
    lowest_metric = min(ragas_metrics, key=lambda x: x[1])
    st.info(
        f"**Diagnosis:** {lowest_metric[0]} ({lowest_metric[1]:.2f}) is the lowest RAGAS metric. "
        f"Consider reducing chunk_size or adding metadata filters to improve retrieval precision."
    )
else:
    st.warning("No RAGAS scores available. Run `python evaluate.py` to compute them.")

st.divider()

# ---------------------------------------------------------------------------
# All Test Cases Table
# ---------------------------------------------------------------------------
st.subheader("📄 All Test Cases")

if results:
    table_data = []
    for r in results:
        table_data.append({
            "ID": r["id"],
            "Dimension": r["dimension"],
            "Question": r["question"][:60] + ("..." if len(r["question"]) > 60 else ""),
            "Passed": "✅" if r.get("judge_passed", False) else "❌",
            "Score": f"{r.get('judge_score', 0.0):.2f}",
            "Latency": f"{r.get('latency', 0.0):.2f}s",
        })

    st.dataframe(table_data, use_container_width=True, hide_index=True)
else:
    st.warning("No detailed results available.")

# ---------------------------------------------------------------------------
# Footer
# ---------------------------------------------------------------------------
st.divider()
st.caption(
    "Generated by the BVRIT FAQ Chatbot Evaluation Suite. "
    "Run `python evaluate.py` to regenerate."
)